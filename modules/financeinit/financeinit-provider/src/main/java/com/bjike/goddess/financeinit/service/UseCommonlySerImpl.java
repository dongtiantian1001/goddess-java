package com.bjike.goddess.financeinit.service;

import com.bjike.goddess.common.api.dto.Restrict;
import com.bjike.goddess.common.api.exception.SerException;
import com.bjike.goddess.common.jpa.service.ServiceImpl;
import com.bjike.goddess.common.provider.utils.RpcTransmit;
import com.bjike.goddess.common.utils.bean.BeanTransform;
import com.bjike.goddess.financeinit.bo.UseCommonlyBO;
import com.bjike.goddess.financeinit.dto.UseCommonlyDTO;
import com.bjike.goddess.financeinit.entity.UseCommonly;
import com.bjike.goddess.financeinit.enums.GuideAddrStatus;
import com.bjike.goddess.financeinit.to.GuidePermissionTO;
import com.bjike.goddess.financeinit.to.UseCommonlyTO;
import com.bjike.goddess.user.api.UserAPI;
import com.bjike.goddess.user.bo.UserBO;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.cache.annotation.CacheConfig;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

import java.time.LocalDateTime;
import java.util.List;

/**
 * 常用摘要业务实现
 *
 * @Author: [ lijuntao ]
 * @Date: [ 2017-10-13 12:56 ]
 * @Description: [ 常用摘要业务实现 ]
 * @Version: [ v1.0.0 ]
 * @Copy: [ com.bjike ]
 */
@CacheConfig(cacheNames = "financeinitSerCache")
@Service
public class UseCommonlySerImpl extends ServiceImpl<UseCommonly, UseCommonlyDTO> implements UseCommonlySer {

    @Autowired
    private UserAPI userAPI;
    @Autowired
    private CusPermissionSer cusPermissionSer;

    /**
     * 核对查看权限（部门级别）
     */
    private void checkSeeIdentity() throws SerException {
        Boolean flag = false;
        String userToken = RpcTransmit.getUserToken();
        UserBO userBO = userAPI.currentUser();
        RpcTransmit.transmitUserToken(userToken);
        String userName = userBO.getUsername();
        if (!"admin".equals(userName.toLowerCase())) {
            flag = cusPermissionSer.getCusPermission("1");
            if (!flag) {
                throw new SerException("您不是相应财务部门的人员，不可以查看");
            }
        }
        RpcTransmit.transmitUserToken(userToken);

    }

    /**
     * 核对添加修改删除审核权限（岗位级别）
     */
    private void checkAddIdentity() throws SerException {
        Boolean flag = false;
        String userToken = RpcTransmit.getUserToken();
        UserBO userBO = userAPI.currentUser();
        RpcTransmit.transmitUserToken(userToken);
        String userName = userBO.getUsername();
        if (!"admin".equals(userName.toLowerCase())) {
            flag = cusPermissionSer.busCusPermission("2");
            if (!flag) {
                throw new SerException("您不是相应财务部门的人员，不可以操作");
            }
        }
        RpcTransmit.transmitUserToken(userToken);

    }

    /**
     * 导航栏核对查看权限（部门级别）
     */
    private Boolean guideSeeIdentity() throws SerException {
        Boolean flag = false;
        String userToken = RpcTransmit.getUserToken();
        UserBO userBO = userAPI.currentUser();
        RpcTransmit.transmitUserToken(userToken);
        String userName = userBO.getUsername();
        if (!"admin".equals(userName.toLowerCase())) {
            flag = cusPermissionSer.getCusPermission("1");
        } else {
            flag = true;
        }
        return flag;
    }

    /**
     * 导航栏核对添加修改删除审核权限（岗位级别）
     */
    private Boolean guideAddIdentity() throws SerException {
        Boolean flag = false;
        String userToken = RpcTransmit.getUserToken();
        UserBO userBO = userAPI.currentUser();
        RpcTransmit.transmitUserToken(userToken);
        String userName = userBO.getUsername();
        if (!"admin".equals(userName.toLowerCase())) {
            flag = cusPermissionSer.busCusPermission("2");
        } else {
            flag = true;
        }
        return flag;
    }

    @Override
    public Boolean sonPermission() throws SerException {
        String userToken = RpcTransmit.getUserToken();
        Boolean flagSee = guideSeeIdentity();
        RpcTransmit.transmitUserToken(userToken);
        Boolean flagAdd = guideAddIdentity();
        if (flagSee || flagAdd) {
            return true;
        } else {
            return false;
        }
    }


    @Override
    public Boolean guidePermission(GuidePermissionTO guidePermissionTO) throws SerException {
        String userToken = RpcTransmit.getUserToken();
        GuideAddrStatus guideAddrStatus = guidePermissionTO.getGuideAddrStatus();
        Boolean flag = true;
        switch (guideAddrStatus) {
            case LIST:
                flag = guideSeeIdentity();
                break;
            case ADD:
                flag = guideAddIdentity();
                break;
            case EDIT:
                flag = guideAddIdentity();
                break;
            case DELETE:
                flag = guideAddIdentity();
                break;
            case COLLECT:
                flag = guideAddIdentity();
                break;
            case IMPORT:
                flag = guideAddIdentity();
                break;
            case EXPORT:
                flag = guideAddIdentity();
                break;
            default:
                flag = true;
                break;
        }

        RpcTransmit.transmitUserToken(userToken);
        return flag;
    }

    @Override
    public Long countUse(UseCommonlyDTO useCommonlyDTO) throws SerException {
        useCommonlyDTO.getConditions().add(Restrict.eq("systemId", getSystemId()));
        Long count = super.count(useCommonlyDTO);
        return count;
    }

    @Override
    public UseCommonlyBO getOneById(String id) throws SerException {
        UseCommonly useCommonly = super.findById(id);
        return BeanTransform.copyProperties(useCommonly, UseCommonlyBO.class);
    }

    @Override
    public List<UseCommonlyBO> listUse(UseCommonlyDTO useCommonlyDTO) throws SerException {
        checkSeeIdentity();
        useCommonlyDTO.getConditions().add(Restrict.eq("systemId", getSystemId()));
        List<UseCommonly> useCommonlies = super.findByCis(useCommonlyDTO);
        return BeanTransform.copyProperties(useCommonlies, UseCommonlyBO.class);
    }

    @Transactional(rollbackFor = {SerException.class})
    @Override
    public UseCommonlyBO addUse(UseCommonlyTO useCommonlyTO) throws SerException {
        checkAddIdentity();
        String systemId = getSystemId();
        UseCommonlyDTO useCommonlyDTO = new UseCommonlyDTO();
        useCommonlyDTO.getConditions().add(Restrict.eq("systemId", getSystemId()));
        useCommonlyDTO.getConditions().add(Restrict.eq("useComm", useCommonlyTO.getUseComm()));
        List<UseCommonly> useCommonlies = super.findByCis(useCommonlyDTO);
        if (useCommonlies != null && useCommonlies.size() > 0) {
            throw new SerException("'" + useCommonlyTO.getUseComm() + "'已存在，不可重复添加");
        }
        UseCommonly useCommonly = BeanTransform.copyProperties(useCommonlyTO, UseCommonly.class, true);
        useCommonly.setCreateTime(LocalDateTime.now());
        useCommonly.setSystemId(systemId);
        super.save(useCommonly);
        return BeanTransform.copyProperties(useCommonly, UseCommonlyBO.class);
    }

    @Transactional(rollbackFor = {SerException.class})
    @Override
    public void delete(String id) throws SerException {
        checkAddIdentity();
        super.remove(id);
    }

    @Override
    public void update(UseCommonlyTO to) throws SerException {
        checkAddIdentity();
        UseCommonly entity = super.findById(to.getId());
        if (null == entity) {
            throw new SerException("指定实体不存在");
        }
        entity.setUseComm(to.getUseComm());
        entity.setModifyTime(LocalDateTime.now());
        super.update(entity);
    }

    /**
     * 获取公司编号
     *
     * @return
     * @throws SerException
     */
    private String getSystemId() throws SerException {
        String token = RpcTransmit.getUserToken();
        String systemId = userAPI.currentSysNO();
        RpcTransmit.transmitUserToken(token);
        return systemId;
    }
}