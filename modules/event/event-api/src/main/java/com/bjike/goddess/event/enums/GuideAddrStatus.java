public enum GuideAddrStatus {

    /**
     * 列表
     */
    LIST(0),
    /**
     * 汇总
     */
    COUNT(1);

    private int code;

    GuideAddrStatus(int code) {
        this.code = code;
    }

    public int getCode() {
        return this.code;
    }

}
